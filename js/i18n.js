// ── Core API ──

export function t(key, params) {
    const dict = translations[_lang] || translations.es;
    let str = dict[key];
    if (str === undefined) str = (translations.es || {})[key];
    if (str === undefined) return key;
    if (params) {
        for (const k of Object.keys(params)) {
            str = str.replaceAll(`{${k}}`, params[k]);
        }
    }
    return str;
}

export function getLang() { return _lang; }

export function setLang(lang) {
    if (!translations[lang]) return;
    _lang = lang;
    localStorage.setItem('sr_lang', lang);
    document.documentElement.lang = lang;
    applyTranslations();
    for (const fn of _listeners) {
        try { fn(lang); } catch (_) { /* noop */ }
    }
}

export function onLangChange(fn) {
    if (typeof fn === 'function') _listeners.push(fn);
}

export function detectLang() {
    const saved = localStorage.getItem('sr_lang');
    if (saved && translations[saved]) return saved;
    const browserLang = (navigator.language || navigator.userLanguage || 'es').slice(0, 2);
    if (translations[browserLang]) return browserLang;
    return 'es';
}

export function initI18n() {
    _lang = detectLang();
    document.documentElement.lang = _lang;
    applyTranslations();
}

export function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (val !== key) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        const val = t(key);
        if (val !== key) el.innerHTML = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const val = t(key);
        if (val !== key) el.title = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const val = t(key);
        if (val !== key) el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        const val = t(key);
        if (val !== key) el.setAttribute('aria-label', val);
    });
}

export function getBuiltinTagLabel(key) {
    const labels = BUILTIN_TAG_LABELS[_lang] || BUILTIN_TAG_LABELS.es;
    return labels[key] || key;
}
